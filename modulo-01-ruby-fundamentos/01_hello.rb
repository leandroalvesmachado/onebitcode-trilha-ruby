# Exibe a saída no terminal sem adicionar uma quebra de linha automática
print "Hello World"

# Como print não adiciona quebra de linha por padrão, você precisa incluir \n manualmente
print "Hello World\n"

# Exibe a saída no terminal e adiciona automaticamente uma quebra de linha ao final
puts "Hello World"

=begin
  Este é um comentário de múltiplas linhas.
  Ele pode abranger várias linhas sem precisar de `#` em cada uma delas.
=end